package com.ilp.tcs.bean;

public class Customer {
	
	private long ssn;
	private long cust_id;
	private String cust_name;
	private String cust_addr;
	private int cust_age;
	private String cust_city;
	private String cust_state;
	private String cust_status;
	private String cust_create_date;
	private String cust_last_updated;
	
	public Customer() {
		
	}
	public long getSsn() {
		return ssn;
	}
	public void setSsn(long ssn) {
		this.ssn = ssn;
	}
	public long getCust_id() {
		return cust_id;
	}
	public void setCust_id(long cust_id) {
		this.cust_id = cust_id;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_addr() {
		return cust_addr;
	}
	public void setCust_addr(String cust_addr) {
		this.cust_addr = cust_addr;
	}
	public int getCust_age() {
		return cust_age;
	}
	public void setCust_age(int cust_age) {
		this.cust_age = cust_age;
	}
	public String getCust_city() {
		return cust_city;
	}
	public void setCust_city(String cust_city) {
		this.cust_city = cust_city;
	}
	public String getCust_state() {
		return cust_state;
	}
	public void setCust_state(String cust_state) {
		this.cust_state = cust_state;
	}
	public String getCust_status() {
		return cust_status;
	}
	public void setCust_status(String cust_status) {
		this.cust_status = cust_status;
	}
	public String getCust_create_date() {
		return cust_create_date;
	}
	public void setCust_create_date(String cust_create_date) {
		this.cust_create_date = cust_create_date;
	}
	public String getCust_last_updated() {
		return cust_last_updated;
	}
	public void setCust_last_updated(String cust_last_updated) {
		this.cust_last_updated = cust_last_updated;
	}
	
	
	

}
